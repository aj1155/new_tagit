package com.nexters.tagit.model;

public class UserModel {
	private String ide_num;
	private String user_id;

	/**
	 * @return the ide_num
	 */
	public String getIde_num() {
		return ide_num;
	}

	/**
	 * @param ide_num
	 *            the ide_num to set
	 */
	public void setIde_num(String ide_num) {
		this.ide_num = ide_num;
	}

	/**
	 * @return the user_id
	 */
	public String getUser_id() {
		return user_id;
	}

	/**
	 * @param user_id
	 *            the user_id to set
	 */
	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}
}
